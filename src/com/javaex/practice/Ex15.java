package com.javaex.practice;

public class Ex15 {
	public static void main(String[] args) {
		
		System.out.println(12 / 5 - 3); // 12/5 == 2 - 3 == -1 
		System.out.println(5 + 19 % 3); // 19 % 3 == 1 + 5 == 6
		System.out.println(2 + 2 / 2); // 2 / 2 == 1 + 2 == 3
		System.out.println(29 % 3 + 29 / 3); // 29 % 3 == 2, 29 / 3 == 9 + 2 == 11
		System.out.println(29 % 3 + 18.0 / 4); // 29 % 3 == 2, 18.0 / 4 == 4.5 + 2 == 6.5
		System.out.println((14 + 7/2) / 4); // 7 / 2 == 3 + 14 == 17 / 4 == 4
		System.out.println((14+7/2.0) / 5 ); // 7 / 2.0 == 3.5 + 14 == 17.5 / 5 == 3.5
		
	}
}
